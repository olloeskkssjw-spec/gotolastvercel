module.exports = async function handler(req, res) {
  res.setHeader("Access-Control-Allow-Origin", "*");
  res.setHeader("Access-Control-Allow-Methods", "POST, OPTIONS");
  res.setHeader("Access-Control-Allow-Headers", "Content-Type");

  if (req.method === "OPTIONS") {
    return res.status(200).end();
  }

  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  const { messages } = req.body;

  if (!Array.isArray(messages) || messages.length === 0) {
    return res.status(400).json({ error: "messages required" });
  }

  try {
    const openai = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });
    const insultVariant = Math.floor(Math.random() * 20) + 1;
    const systemWithSeed =
      SYSTEM_PROMPT +
      `\n\n[Если это оскорбление/мат — используй вариант #${insultVariant} из списка выше. Обязательно.]`;

    const completion = await openai.chat.completions.create({
      model: "gpt-4o-mini",
      messages: [
        { role: "system", content: systemWithSeed },
        ...messages.slice(-12),
      ],
    });

    const reply =
      completion.choices[0]?.message?.content || "Акула думает... 🦈";
    return res.json({ reply });
  } catch (err) {
    console.error("Shark chat error:", err);
    return res.status(500).json({ error: "Акула временно недоступна 🦈" });
  }
};
